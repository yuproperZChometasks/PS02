import requests

# URL открытого API jsonplaceholder для получения постов
url = "https://jsonplaceholder.typicode.com/posts"

# Параметры запроса: userId равен 1
params = {
    'userId': 1
}

# Отправляем GET-запрос
response = requests.get(url, params=params)

# Печатаем статус-код ответа
print("Статус-код ответа:", response.status_code)

# Печатаем содержимое ответа в формате JSON
if response.ok:
    data = response.json()
    print("Полученные записи:")
    for post in data:
        print(post)
else:
    print("Произошла ошибка при запросе данных.")

